# # Python Logic Practice Set

# ## Section A: Warm-Up (Very Basic)

# Write full programs for each.

# ### Q1. Even or Odd


# Take an integer input and print:

# * `"Even"` if the number is even
# * `"Odd"` if the number is odd

# ---

# ### Q2. Sum of First N Numbers

# Take an integer `n` and print the sum from `1` to `n`.

# Example:

# ```
# Input: 5
# Output: 15
# ```

# ---

# ### Q3. Largest of Three Numbers

# Take three numbers as input and print the largest one.

# ---

# ### Q4. Reverse a String

# Take a string as input and print the reversed string.

# Example:

# ```
# Input: hello
# Output: olleh
# ```

# ---

# ## Section B: Core Logic (Very Common in Interviews)

# ### Q5. Count Vowels

# Take a string input and count how many vowels it contains.

# Example:

# ```
# Input: interview
# Output: 4
# ```

# ---

# ### Q6. Palindrome Check

# Take a string and print:

# * `"Palindrome"` if it is a palindrome
# * `"Not Palindrome"` otherwise

# ---

# ### Q7. Factorial of a Number

# Take an integer input and print its factorial.

# Example:

# ```
# Input: 5
# Output: 120
# ```

# ---

# ### Q8. Sum of List Elements

# Take a list of numbers and print the sum.

# Example:

# ```
# nums = [10, 20, 30]
# Output: 60
# ```

# ---

# ## Section C: Slightly Intermediate

# ### Q9. Prime Number Check

# Take a number and print:

# * `"Prime"` if it is prime
# * `"Not Prime"` otherwise

# ---

# ### Q10. Second Largest Number

# Take a list of numbers and print the second largest element.

# Example:

# ```
# Input: [10, 5, 8, 20]
# Output: 10
# ```

# ---

# ### Q11. Count Frequency of Each Character

# Take a string and print the frequency of each character.

# Example:

# ```
# Input: apple
# Output:
# a:1
# p:2
# l:1
# e:1
# ```

# ---

# ### Q12. Remove Duplicates from a List

# Take a list and print it after removing duplicates.

# Example:

# ```
# Input: [1, 2, 2, 3, 1, 4]
# Output: [1, 2, 3, 4]
# ```

